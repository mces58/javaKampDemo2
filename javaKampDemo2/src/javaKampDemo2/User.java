package javaKampDemo2;

public class User {
	
	int userId;
	String userName;
	String userPassword;
	String userEmail;
	
	
	public User(int userId, String userName, String userPassword, String userEmail) {

		this.userId = userId;
		this.userName = userName;
		this.userPassword = userPassword;
		this.userEmail = userEmail;
	}


}
